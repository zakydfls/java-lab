package latihan_1;

public class Matematika {
    public int pertambahan(int a, int b) {
        int hasil = a + b;
        return hasil;
    }

    public int pengurangan(int a, int b) {
        return a - b;
    }

    public int pengalian(int a, int b) {
        return a * b;
    }

    public int pembagian(int a, int b) {
        return a / b;
    }

    public double pertambahan(double a, double b, double c) {
        return a + b + c;
    }

    public double pengurangan(double a, double b, double c) {
        return a - b - c;
    }

    public double pembagian(double a, double b, double c) {
        return a / b / c;
    }

    public double pengalian(double a, double b, double c) {
        return a * b * c;
    }

}
